# Internal helpers
# ---------------------------------------------------------------------------

def _reindent(text: str, target_indent: int) -> str:
    """Re-indent a block of source text to the given indent level.

    Strips the common leading whitespace from all non-empty lines, then
    re-applies ``target_indent * 4`` spaces.

    Args:
        text: Multi-line source string.
        target_indent: Number of 4-space indent levels to apply.

    Returns:
        Re-indented source string.
    """
    lines = text.splitlines()
    if not lines:
        return "    " * target_indent + "pass"

    # Find minimum indentation of non-empty lines
    non_empty = [ln for ln in lines if ln.strip()]
    if not non_empty:
        return "    " * target_indent + "pass"

    min_indent = min(len(ln) - len(ln.lstrip()) for ln in non_empty)
    pad = "    " * target_indent

    result_lines: list[str] = []
    for ln in lines:
        if ln.strip():
            result_lines.append(pad + ln[min_indent:])
        else:
            result_lines.append("")

    return "\n".join(result_lines)
